  });

  @override
  State<SilverAppBarWidgets> createState() => _SilverAppBarWidgetsState();
}

class _SilverAppBarWidgetsState extends State<SilverAppBarWidgets> {


  // for storing search status
  bool _isSearching = false;
  @override
  Widget build(BuildContext context) {
    return SliverAppBar(
      automaticallyImplyLeading: false,
      title: const Text(
        "WhatsApp",
        style: TextStyle(fontSize: 22, color: Colors.white, letterSpacing: 1),
      ),
      actions: [
        IconButton(
          onPressed: () {},
          icon: const Icon(
            Icons.camera_alt_outlined,
            color: Colors.white,
          ),
        ),
        IconButton(
          onPressed: () {

           setState(() {
             _isSearching = !_isSearching;
           });


          },
          icon: const Icon(
            Icons.search_outlined,
            color: Colors.white,
          ),
        ),

        PopMenuButtonWidgets()
      ],
      floating: true,
      pinned: true,
      bottom:  TabBar(
        tabs: [
          Tab(
            icon: Icon(
              Icons.people_alt_outlined,
            ),
          ),
          TabWidgets(title: 'Chats', radius: 8, ),
          TabWidgets(title: 'Status', radius: 3, ),
          TabWidgets(title: 'Calls', radius: 3, ),

        ],
      ),
    );
  }
}
